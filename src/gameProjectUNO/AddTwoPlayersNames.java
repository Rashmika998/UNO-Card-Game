    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed

        if(jTextField1.getText().isEmpty()) {
            JLabel message = new JLabel("Please Enter a name!");
            message.setFont(new Font("Arial",Font.BOLD,42));
            JOptionPane.showMessageDialog(null,message);
        }
        else {
            String name = jTextField1.getText().trim();
            playerNames.add(name);
            if(playerNames.size()==1)
                jLabel3.setText(playerNames.get(0));

            else if(playerNames.size()==2){
                jLabel3.setText(playerNames.get(0));
                jLabel5.setText(playerNames.get(1));
            }

            if (playerNames.size()>0 && playerNames.size()<3){
                PlayGame pg= new PlayGame();
                try {
                       pg.playMusicStarts("sounds/welcome.wav");
                } catch (IOException ex) {
                       Logger.getLogger(GameMenu.class.getName()).log(Level.SEVERE, null, ex);
                } catch (LineUnavailableException ex) {
                       Logger.getLogger(GameMenu.class.getName()).log(Level.SEVERE, null, ex);
                } catch (UnsupportedAudioFileException ex) {
                       Logger.getLogger(GameMenu.class.getName()).log(Level.SEVERE, null, ex);
                }
                ImageIcon icon=new ImageIcon(AddPlayersNames.class.getResource("UNOimages/challenger/challenger.png"));
                JLabel message = new JLabel("Welcome to UNO "+playerNames.get(p));
                message.setFont(new Font("Arial",Font.BOLD,42));
                JOptionPane.showMessageDialog(null,message,"Welcome Message",JOptionPane.PLAIN_MESSAGE,icon);
                jTextField1.setText("");
                
                p++;
            }
            if(playerNames.size()==3){
                playerNames.remove(name);
                JLabel message = new JLabel("Whoops there can be only 2 players");
                message.setFont(new Font("Arial",Font.BOLD,42));
                JOptionPane.showMessageDialog(null,message);
                jTextField1.setText("");
            }
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

