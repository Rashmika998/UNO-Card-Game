    private void DoneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DoneButtonActionPerformed
        if (playerNames.size()==1 || playerNames.size()==0){
            JLabel message = new JLabel("Whoops! there should be atleast two players");
            message.setFont(new Font("Arial",Font.BOLD,42));
            JOptionPane.showMessageDialog(null,message);
            jTextField1.setText("");
        }
        else{
            PlayGame pg= new PlayGame();
        try {
            pg.playMusicStarts("sounds/click.wav");
        } catch (IOException ex) {
            Logger.getLogger(GameMenu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(GameMenu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedAudioFileException ex) {
            Logger.getLogger(GameMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
            this.dispose();
            new MultiPlayerGameStage(playerNames,score).setVisible(true);
        }

    }//GEN-LAST:event_DoneButtonActionPerformed

